public class Fordon {
    public String märke;
    public int hastighet;
    public String färg;
    public boolean fordonIgång;

    Fordon(String märke,int hastighet, String färg,boolean fordonIgång){
        this.märke = märke;
        this.hastighet = hastighet;
        this.färg = färg;
        this.fordonIgång = fordonIgång;
    }
}
